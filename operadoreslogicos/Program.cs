// Operadores lógicos
bool x = true;
bool y = false;

bool and = x && y; // E
bool or = x || y; // OU
bool not = !x; // NÃO

// Exibindo resultados dos operadores lógicos
Console.WriteLine("\nOperadores Lógicos");
Console.WriteLine($"x && y: {and}");
Console.WriteLine($"x || y : {or}");
Console.WriteLine($"!x: {not}");
